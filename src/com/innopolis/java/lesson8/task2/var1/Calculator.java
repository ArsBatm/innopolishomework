package innopolis.java.lesson8.task2.var1;

public class Calculator {
//  Объявление статических методов операций
    static  double plus (double firstNumber, double secondNumber){
        return firstNumber + secondNumber;
    }

    static  double minus (double firstNumber, double secondNumber){
        return firstNumber - secondNumber;
    }

    static  double multi (double firstNumber, double secondNumber){
        return firstNumber * secondNumber;
    }

    static  double div (double firstNumber, double secondNumber){
        return firstNumber / secondNumber;
    }

    static  double per (double firstNumber, double secondNumber){
        return firstNumber % secondNumber;
    }
}
